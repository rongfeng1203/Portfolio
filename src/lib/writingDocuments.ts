const paragraphs = (...texts: string[]): WritingBlock[] =>
  texts.map((text) => ({ type: "paragraph", text }));

const heading = (text: string, level: 2 | 3 = 2): WritingBlock => ({
  type: "heading",
  level,
  text,
});

const emphasis = (text: string): WritingBlock => ({ type: "emphasis", text });
const divider = (): WritingBlock => ({ type: "divider" });

